import java.io.*;
import java.util.*;

public class Main {

    public static void main(String[] args) throws Exception {
        // Input & Output stream
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));


        // close the buffer
        br.close();
        bw.close();
    }
}